// Crear un programa que me permita determinar el día de la semana

const numeroDia = 1;

switch(numeroDia) {
	case 1:
		console.log("Lunes");
		break;
	case 2:
		console.log("Martes");
		break;
	case 3:
		console.log("Miercoles");
		break;
	case 4:
		console.log("Jueves");
		break;
	case 5:
		console.log("Viernes");
		break;
	case 6:
		console.log("Sabado");
		break;
	case 7:
		console.log("Domingo");
		break;
	default:
		console.log("Este día no es válido");
}
