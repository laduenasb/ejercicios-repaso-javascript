// Crear un programa que me ayude a determinar el tipo de vehiculo

const tipo = "camión";

switch (tipo) {
	case "motocicleta":
		console.log("Esta es una motocicleta");
		break;
	case "helicóptero":
		console.log("Este es un helicoptero");
		break;
	case "camión":
		console.log("Este es un camión");
		break;
	default:
		console.log("No se reconoce el tipo de vehículo");

}